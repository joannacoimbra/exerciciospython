# Faça um Programa que peça as 4 notas bimestrais e mostre a média.
w= float(input("informe a nota do 1° bimestre: "))
x= float(input("informe a nota do 2° bimestre: "))
y= float(input("informe a nota do 3° bimestre: "))
z= float(input("informe a nota do 4° bimestre: "))
print ("a média dos quatro bimestres é: ", (w+x+y+z)/4)